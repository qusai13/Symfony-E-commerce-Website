    public function findwithparent()
    {
        $conn = $this->getEntityManager()->getConnection();
        $sql = 'SELECT cat1.*,cat2.title as cat_title  from category cat1  left join category cat2 on cat1.parentid = cat2.id';
        $stmt = $conn->prepare($sql);
        $stmt->execute();
        return $stmt->fetchAll();
    }

